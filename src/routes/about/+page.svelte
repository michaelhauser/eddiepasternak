			work has been a welcome feature in swing-oriented performances with former members of Count
			Basie and Duke Ellington’s bands as well as in Motown dance bands. He has frequently performed
			in the Bay Area’s finest hotels and major local events. Noted Bay Area music critic Lee
			Hiderbrand describes him as a “remarkable guitarist who plays basslines and chords
			simultaneously. He generates intense swing.“
